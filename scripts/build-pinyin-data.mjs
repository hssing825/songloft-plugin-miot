#!/usr/bin/env node
// Generate a static CJK pinyin lookup table at build time.
//
// QuickJS on ARM has no JIT and pays the full module initialization cost for
// pinyin-pro dictionaries during import. Keeping the heavy dependency here lets
// the plugin runtime do cheap object lookups instead.

import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { pinyin } from 'pinyin-pro';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const OUT_DIR = join(ROOT, 'src', 'data');
const OUT_FILE = join(OUT_DIR, 'pinyin-map.ts');

const CJK_UNIFIED_START = 0x4e00;
const CJK_UNIFIED_END = 0x9fff;

function charPinyin(ch) {
  const result = pinyin(ch, {
    toneType: 'none',
    type: 'array',
    nonZh: 'consecutive',
  });
  const value = Array.isArray(result) ? String(result[0] || '') : String(result || '');
  const py = value.trim().toLowerCase();
  return py && py !== ch ? py : '';
}

function main() {
  const entries = [];
  for (let cp = CJK_UNIFIED_START; cp <= CJK_UNIFIED_END; cp++) {
    const ch = String.fromCodePoint(cp);
    const py = charPinyin(ch);
    if (py) {
      entries.push([ch, py]);
    }
  }

  mkdirSync(OUT_DIR, { recursive: true });

  const lines = [
    '// AUTO-GENERATED by scripts/build-pinyin-data.mjs - DO NOT EDIT',
    '// Generated from pinyin-pro at build time to avoid loading dictionaries in QuickJS.',
    '',
    'export const pinyinMap: Record<string, string> = {',
  ];

  for (const [ch, py] of entries) {
    lines.push(`  ${JSON.stringify(ch)}: ${JSON.stringify(py)},`);
  }

  lines.push('};');
  lines.push('');

  writeFileSync(OUT_FILE, lines.join('\n'));
  console.log(`[build-pinyin-data] wrote src/data/pinyin-map.ts (${entries.length} chars)`);
}

main();
